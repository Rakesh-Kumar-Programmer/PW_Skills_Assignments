// use of Ternary Operator

const num1 = 4; // value 4 is assign to num1
const num2 = 3; // value 3 is assign to num2

let result = num1 % 2 == 0 ? 'even' : 'odd' ;  // here we check condition via ternary oprator and find condition is true
console.log(`the value num1 is : ${result}`); // result : even

result = num2 % 2 == 0 ? 'even' : 'odd' ; // here we check condition via ternary oprator and find condition is false
console.log(`the value num2 is : ${result}`); // result : odd