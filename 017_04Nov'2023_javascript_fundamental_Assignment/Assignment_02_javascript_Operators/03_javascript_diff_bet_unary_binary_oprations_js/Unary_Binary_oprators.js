// Plus
let numStr ="25";
let sum = +numStr; // converts "25" to the number 25
console.log(sum);

// negation

let numStr1 ="56";
let sum1 = +numStr1; // Converts "56" to the number 56
console.log(sum1);

// Logical Not ('!')
let sunny = false;
let rainy = !sunny; // reverse thr value 
console.log(rainy); // output : true

//Increment (++) and Decrement (--) oprators
// Increment oprator
let count = 6;
count++; // increment
console.log(count); // output :7

//Decrement oprator
let count1 = 11;
count1--; //Decrease 
console.log(count1); // output : 10

